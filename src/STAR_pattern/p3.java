package STAR_pattern;

public class p3 {

	
	public static void main(String[] args) {
		
		
		
		
		int star=0;
		int space=10;
		
		for(int i=1; i<=10; i++)
		{
			
			
			
			
			for(int j=1; j<=star; j++)
			{
				
				System.out.print("*");
			}
			
			for(int j=1; j<=space; j++)
			{
				
				System.out.print(" ");
			}
			
			System.out.println();
			star++;
			space--;
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
